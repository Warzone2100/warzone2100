function chooseResearch(){
//	var _research = debugName;
	if(typeof forceResearch !== 'undefined'){
		var _research = forceResearch;
		setResearchWay(_research);
	}else{
		var _w=["Black","Green","Red","Yellow","Orange","Blue","Rich"];
	//	if(difficulty == HARD || difficulty == INSANE) _w=["Green","Orange"];
		if(difficulty == HARD || difficulty == INSANE) _w=["Green","Orange","Yellow","Rich","Orange","Green","Green"];
		if(bc_ally.length == 1 && policy['build'] == 'rich' && difficulty == MEDIUM) _w = ['Rich'];
		var _r = 0;
		debugMsg("bc_ally="+bc_ally.length, 'research');
		if(bc_ally.length > 1){
			_r=bc_ally.indexOf(me)%_w.length;
			debugMsg("way="+_r, 'research_way');
		}else{
			_r=Math.floor(Math.random()*_w.length);
		}
		setResearchWay(_w[_r]);
	}
}


function setResearchWay(way){
	rWay = way;
	if(way == "Black"){
		research_primary = research_black;
		policy['build'] = 'cyborgs';
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
	if(way == "Rich"){
		research_primary = research_rich;
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
	if(way == "Green"){
		research_primary = research_green;
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
	if(way == "Red"){
		research_primary = research_red;
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
	if(way == "Yellow"){
		research_primary = research_yellow;
		//	researchStrategy = 'Strict';
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
	if(way == "Orange"){
		research_primary = research_orange;
		//	researchStrategy = 'Strict';
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}

	if(way == "Blue"){
		research_primary = research_blue;
		//	researchStrategy = 'Strict';
		debugMsg("Использую тестовый путь исследований "+way, 'init');
		return;
	}
}



