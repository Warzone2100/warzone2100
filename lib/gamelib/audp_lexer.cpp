	n = _yybytes_len + 2;
	buf = (char *) audp_alloc(n  );
	if ( ! buf )
		YY_FATAL_ERROR( "out of dynamic memory in audp__scan_bytes()" );

	for ( i = 0; i < _yybytes_len; ++i )
		buf[i] = yybytes[i];

